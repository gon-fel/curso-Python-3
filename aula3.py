#Introdução aos Tipos de Dados
"""Qualquer número inteiro será do tipo int. 
float => é igual a qualquer número decimal, na linguagem de programação esses número sempre serão separados por "ponto".

Existe a classe 'type' que vai mostrar o que o Python inferiu o valor"""
print(type('Augusto')) #ele indica o tipo de classe que aquele argumento pertence.

print(type(1)) #indica aqui que é do tipo 'int'.
print(type(1.2)) #indica que é do tipo 'float'.

"""Posso colocar a classe 'type' quantas vezes eu quiser, ele sempre vai me apontar a que clase aquele argumento pertence."""

print(type('algusto'), type(1), type(3.5)) #Nesse caso eu especifiquei o que queria para cada argumento, mas deve ter uma forma de colocar uma única vez a classe. Talvez eu aprenda no futuro.